public class DetailedList {
    private String name,house,points,position;

    public DetailedList(){
        this.name = name;
        this.house = house;
        this.points = points;
        this.position = position;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHouse() {
        return house;
    }

    public void setHouse(String house) {
        this.house = house;
    }

    public String getPoints() {
        return points;
    }

    public void setPoints(String points) {
        this.points = points;
    }

    public String getPositiin() {
        return position;
    }

    public void setPositiin(String positiin) {
        this.position = positiin;
    }
}
